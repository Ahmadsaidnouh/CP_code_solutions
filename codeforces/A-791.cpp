#include <iostream>
using namespace std;

int main()
{
    int bearAge, brotherAge;
    cin >> bearAge >> brotherAge;
    if(bearAge > brotherAge)
    {
        cout << "Error: invalid input";
        return 1;
    }

    int years = 0;
    while (bearAge <= brotherAge) 
    {
        years++;
        bearAge *= 3;
        brotherAge *= 2;
    }
    cout << years;
    return 0;
}
